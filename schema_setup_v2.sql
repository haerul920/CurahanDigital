-- ── 1. Profiles ──────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.curhatwall_profiles (
  id          TEXT PRIMARY KEY,          -- Clerk User ID
  username    TEXT NOT NULL UNIQUE,
  full_name   TEXT,
  avatar_seed TEXT,
  hide_email  BOOLEAN DEFAULT false,
  created_at  TIMESTAMPTZ DEFAULT now() NOT NULL
);

-- ── 2. Posts ─────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.curhatwall_posts (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id      TEXT NOT NULL REFERENCES public.curhatwall_profiles(id) ON DELETE CASCADE,
  content      TEXT NOT NULL CHECK (char_length(content) >= 10 AND char_length(content) <= 500),
  is_anonymous BOOLEAN DEFAULT true NOT NULL,
  created_at   TIMESTAMPTZ DEFAULT now() NOT NULL
);

-- ── 3. Likes ─────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.curhatwall_likes (
  id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id    TEXT NOT NULL REFERENCES public.curhatwall_profiles(id) ON DELETE CASCADE,
  post_id    BIGINT NOT NULL REFERENCES public.curhatwall_posts(id) ON DELETE CASCADE,
  created_at TIMESTAMPTZ DEFAULT now() NOT NULL,
  UNIQUE (user_id, post_id)              -- one like per user per post
);

-- ── 4. Comments ──────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.curhatwall_comments (
  id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  post_id    BIGINT NOT NULL REFERENCES public.curhatwall_posts(id) ON DELETE CASCADE,
  user_id    TEXT NOT NULL REFERENCES public.curhatwall_profiles(id) ON DELETE CASCADE,
  content    TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT now() NOT NULL
);

-- ── 5. Enable Row Level Security ─────────────────────────────────────────────
ALTER TABLE public.curhatwall_profiles ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.curhatwall_posts    ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.curhatwall_likes    ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.curhatwall_comments ENABLE ROW LEVEL SECURITY;

