-- ── 10. Helper View: per-user stats (used by dashboard) ──────────────────────
-- Returns for a given user:
--   my_posts        = number of posts they authored
--   my_likes_received = total likes on all their posts
--   my_comments_received = total comments on all their posts
CREATE OR REPLACE VIEW public.curhatwall_user_stats AS
SELECT
  p.id                                                  AS user_id,
  COUNT(DISTINCT po.id)                                 AS my_posts,
  COUNT(DISTINCT l.id)                                  AS my_likes_received,
  COUNT(DISTINCT c.id)                                  AS my_comments_received
FROM public.curhatwall_profiles p
LEFT JOIN public.curhatwall_posts    po ON po.user_id = p.id
LEFT JOIN public.curhatwall_likes    l  ON l.post_id  = po.id
LEFT JOIN public.curhatwall_comments c  ON c.post_id  = po.id
GROUP BY p.id;

